// ตัวดำเนินการเปรียบเทียบ
let c = 5 , d = 10 ;
let check = false;

console.log("ตัวดำเนินการเปรียบเทียบ");
console.log("5 = 10 :",c == d);
console.log("5 != 10 :",c != d);
console.log("5 > 10 :",c > d);
console.log("5 < 10 :",c < d);
console.log("5 >= 10 :",c >= d);
console.log("5 <= 10 :",c <= d);

console.log("check =",check);
console.log("!check",!check);